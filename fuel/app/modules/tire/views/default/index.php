			<p id="r_eco_txt"><span class="greb">低燃費タイヤとは？</span><span class="redb">ラベリング制度が制定する基準を満たしたタイヤ</span>ラベリング制度は、グレーティングシステム（等級制度）に基づき、「転がり抵抗性能」と「ウェットグリップ性能」の等級分けを行い、ラベル表示するものです。つまり、左のマークが付けられたタイヤが「低燃費タイヤ」です。<br /><br /><span class="redb">グレーティングシステム（等級制度）について</span>少ない燃料で、より遠くまで走る為には「転がり抵抗」が低いほど良いのですが、「転がり抵抗」が低すぎるとグリップ力が弱まり、濡れた路面での制動距離が伸びてしまいます。つまり、低燃費タイヤのマークが付くタイヤは、「転がり抵抗性能」と「ウェットグリップ性能（制動時のグリップ力）」の双方が基準を満たす必要があります。<br />「転がり抵抗性能」の等級がA以上で、「ウェットグリップ性能」の等級がａ～ｄの範囲内にあるタイヤを「低燃費タイヤ」と言います。</p>

        <h2 class="btit">タイヤの基礎知識</h2>
		<p class="sub">タイヤはゴムでできているので、古くなり劣化が進むとゴムが硬化してヒビ割れを起こします。また、タイヤには寿命があり、寿命を知らせるスリップサインが出たタイヤでの走行は、道路交通法の違反となります。また、擦り減ったタイヤでの走行は大変危険です。</p>
